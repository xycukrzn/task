// Задача 38: Задайте массив вещественных чисел. Найдите разницу между максимальным и минимальным элементов массива.

{
double[] numbers = { 3.5, 1.2, 4.8, 2.1, 6.0, 5.3, 9.7, 7.4 }; // Задаем массив вещественных чисел

// Находим максимальный и минимальный элементы массива
double max = numbers[0]; // Предполагаем, что первый элемент является максимальным
double min = numbers[0]; // Предполагаем, что первый элемент является минимальным

foreach (double number in numbers)
{
    if (number > max)
    {
        max = number; // Найден новый максимальный элемент
    }

    if (number < min)
    {
        min = number; // Найден новый минимальный элемент
    }
}

// Находим разницу между максимальным и минимальным элементами
double difference = max - min;

// Выводим результат на экран
Console.WriteLine("Максимальный элемент: " + max);
Console.WriteLine("Минимальный элемент: " + min);
Console.WriteLine("Разница между максимальным и минимальным элементами: " + difference);
}